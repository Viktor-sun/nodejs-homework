const { Schema, model } = require('mongoose')

const schemaContacts = new Schema({
  name: {
    type: String,
    required: [true, 'Set name for contact'],
  },
  email: {
    type: String,
  },
  phone: {
    type: String,
  },
  favorite: {
    type: Boolean,
    default: false,
  },
})

const Contact = model('contact', schemaContacts)

module.exports = Contact
